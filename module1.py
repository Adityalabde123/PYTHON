def areacy(r,h):
    a=2*r*(r+h)
    print("Area of cylinder=",a)
    v=r**2*h
    print("Volume of cylinder=",v)
def areacu(l,w,h):
    a=2 * (l*w +l*h+w*h)
    print("Area of cubiod=",a)
    v=l*w*h
    print("volume of cuboid=",v)
